package StringProgrames;

public class Alphabets {

	public static void main(String[] args) {
		for(int i=97;i<123;i++){
			char ch=(char)i;
			System.out.print(ch+" ");
		}
		System.out.println();
		for(int i=65;i<91;i++){
			
			System.out.print((char)i+" ");
		}
		System.out.println();
		for(char ch='A';ch<='Z';ch++){
			
			System.out.print(ch+" ");
		}

	}

}
